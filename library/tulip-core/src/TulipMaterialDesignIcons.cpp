bool TulipMaterialDesignIcons::isMaterialDesignIconSupported(const std::string &iconName) {
  if (materialDesignIconCodePoint.empty()) {
    initMaterialDesignIconCodePoints();
  }

  return materialDesignIconCodePoint.find(iconName.c_str()) != materialDesignIconCodePoint.end();
}

const vector<std::string> &TulipMaterialDesignIcons::getSupportedMaterialDesignIcons() {
  if (materialDesignIconCodePoint.empty()) {
    initMaterialDesignIconCodePoints();
  }

  return materialDesignIconsNames;
}

std::string TulipMaterialDesignIcons::getMaterialDesignIconUtf8String(const std::string &iconName) {
  if (materialDesignIconCodePoint.empty()) {
    initMaterialDesignIconCodePoints();
  }

  std::string iconString;
  utf8::utf32to8(materialDesignIconCodePoint[iconName.c_str()].begin(), materialDesignIconCodePoint[iconName.c_str()].end(),
                 back_inserter(iconString));
  return iconString;
}

unsigned int TulipMaterialDesignIcons::getMaterialDesignIconCodePoint(const std::string &iconName) {
  if (materialDesignIconCodePoint.empty()) {
    initMaterialDesignIconCodePoints();
  }

  map<const char *, vector<unsigned int>, cmpCharPtr>::iterator it = materialDesignIconCodePoint.find(iconName.c_str());

  if (it != materialDesignIconCodePoint.end())
    return (it->second[0]);

  return 0;
}
